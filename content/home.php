			<h3>Das Heidelberger Symposium <small>8. – 10. Mai 2014</small></h3>
				<p>Das 26. Heidelberger Symposium mit dem Thema <strong>Grenzen[los]</strong> findet vom 8. bis 10. Mai 2014 auf dem Heidelberger Uniplatz und an der Universität Heidelberg statt. An drei Tagen werden dabei zahlreiche Vorträge, Podiumsdiskussionen und Kolloquien veranstaltet, die die Thematik der Grenzen/Grenzenlosigkeit in unserer Gesellschaft von möglichst vielen Blickwinkeln aus durchleuchten sollen. So werden beim Symposium Vertreter sowohl der Geisteswissenschaften, als auch aus Politik, Raumfahrt, Medizin und vielen anderen Disziplinen anwesend sein. Dabei sollen zwischen den verschiedenen Disziplinen eben keine Grenzen gezogen werden und ein reger interdisziplinärer Austausch herbeigeführt werden. Neben den wissenschaftlichen Beiträgen werden auch zahlreiche kulturelle Veranstaltungen angeboten und die Teilnehmer des Symposiums während den drei Tagen kostenlos kulinarisch versorgt. Das 26. Heidelberger Symposium wird von Frank Plasberg als Schirmherr begleitet</p>
				<p>Neben Frank Plasberg werden unter anderem Ernst Uhrlau, Jörg Armbruster, Dr. Paul Kirchhof, Prof. Dr. Dr. Ernst Messerschmid, Dr. Franziska Brantner und Samuel Koch für spannende Vorträge und Diskussionen sorgen. Nach über 1000 Teilnehmern im Jahr 2013 verspricht auch das 26. Heidelberger Symposium ein voller Erfolg zu werden.</p>
				<p>Das Heidelberger Symposium wird jährlich vom Heidelberger Club für Wirtschaft und Kultur veranstaltet, einer gemeinnützigen Studenteninitiative die 1988 mit dem Ziel gegründet wurde, die Ausbildung an den Universitäten durch Praxisbezug und interdisziplinären Austausch zu ergänzen.</p>
