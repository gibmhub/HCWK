<h2>Informationen zur Teilnahme</h2>
<p>Wie würdest du unser Thema Grenzen(los) auf einem Plakat, auf Flyern und in einem Logo darstellen?</p>
<p>Der Heidelberger Kunst und Kulturpreis findet dieses Jahr zum achten Mal statt. Deutschlandweit bekommen Kreative die Gelegenheit ein Designpaket für das 26. Heidelberger Symposium unter dem Thema Grenzen(los) zu gestalten. Das 26. Heidelberger Symposium findet vom 08. bis 10. Mai 2014 an der Universität Heidelberg statt. Auf den glücklichen Gewinner warten 500€ Preisgeld und eine Ehrung auf dem Heidelberger Symposium. Das Gewinnerplakat wird überall in Heidelberg und Umgebung ausgehangen um Menschen für das Symposium zu begeistern. Der zweite und dritte Platz erhalten Sachpreise.</p>
<p>Weitere Informationen zum Symposium sind auf der Homepage des Heidelberger Clubs für Wirtschaft und Kultur (www.hcwk.de) und auf Facebook zu finden. Das Design soll der Veranstaltung und der Zielgruppe entsprechen: seriös, klug und innovativ-kreativ.</p>
<h2>Teilnahmebedingungen:</h2>
<ul class="text">
<li>Das Plakat soll Din A1- Format haben, die Flyer DIN A6</li>
<li>Einsendeschluss ist der 14. Februar 2014 (Poststempel)</li>
<li>Bei Einsendung per E-Mail muss die Datei mit Nachname und Vorname benannt sein.</li>
<li>Das Designpaket muss eigens für diesen Anlass und nicht von Dritten erstellt worden
sein.</li>
<li>Mit der Einsendung erklärt der Teilnehmer, dass der HCWK e.V. das ihm zugesandte
Material zeitlich und umfänglich unbegrenzt verwenden darf. Verwertungsansprüche
entstehen im beiderseitigen Einvernehmen nicht.</li>
<li>Der Einsendung muss ein ausgefülltes Anmeldeformular beigefügt sein.</li>
<li>Der Rechtsweg ist ausgeschlossen. Sachpreise können nicht ausgezahlt werden.</li>
</ul>
